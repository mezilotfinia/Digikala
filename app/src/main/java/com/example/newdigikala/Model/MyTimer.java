package com.example.newdigikala.Model;

public class MyTimer{
	private int sec;
	private int min;
	private int hour;

	public void setSec(int sec){
		this.sec = sec;
	}

	public int getSec(){
		return sec;
	}

	public void setMin(int min){
		this.min = min;
	}

	public int getMin(){
		return min;
	}

	public void setHour(int hour){
		this.hour = hour;
	}

	public int getHour(){
		return hour;
	}
}
